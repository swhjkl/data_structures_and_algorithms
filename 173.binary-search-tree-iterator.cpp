/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode() : val(0), left(nullptr), right(nullptr) {}
 *     TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
 *     TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
 * };
 */
class BSTIterator {
public:
    BSTIterator(TreeNode* root) {
        while (root) {
            s.push(root);
            root = root->left;
        }
    }
    
    int next() {
        auto node = s.top();
        s.pop();
        int val = node->val;
        node = node->right;
        while (node) {
            s.push(node);
            node = node->left;
        }
        return val;
    }
    
    bool hasNext() {
        return !s.empty();
    }

private:
    stack<TreeNode*> s;
};

